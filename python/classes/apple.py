class Apple:
    def __init__(self):
        self.color = "red"
        self.flavor = "sweet"

honeycrisp = Apple()
print(honeycrisp.color)

# prints "red"
#__ init__ is a constructor
# __init__ constructor, special methods begin and end with a double underscore,
#  and this is called dunder method. The word “dunder” combines the “d” in double
#   and the “under” in underscore.